typedef struct __pci_vendor_info {
	WORD			vendor_id;			/* vendor id */
	WORD			device_id;			/* device id */
	const char		*name;			/* device name */
	BYTE			bridge_type;	/* bridge type or 0xff */
} pci_vendor_info;

typedef struct __pci_device_info {
	pci_vendor_info		info;
	DWORD				base_addr;
	BYTE				base_class;
	BYTE				sub_class;
} pci_device_info;

typedef struct __pci_res {
	BYTE		bus;		/* バス番号 */
	BYTE		dev;		/* デバイス番号 */
	BYTE		fn;			/* 機能番号 */
} pci_reg;

typedef struct __pci_dev {
	int						pci_num;
	struct __pci_dev		*next;
	DWORD					mem_addr[6];
	DWORD					mem_size[6];
	WORD					io_addr[6];
	WORD					io_size[6];
	BYTE					irq_num;
	BYTE					intr_num;
	WORD					vendor_id;
	WORD					device_id;
	WORD					subsys_id;
	pci_vendor_info			*vendor;
	pci_reg					dev;
} pci_dev;

